#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	c;

	c = 0;
	if (s && f)
	{
		while (s[c])
		{
			f(c, &s[c]);
			c++;
		}
	}
}
